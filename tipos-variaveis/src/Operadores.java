public class Operadores {
    public static void main(String[] args) {
        
       // repetição
       int numero = 5;
       //numero = numero + 2;

       System.out.println(numero ++);

       System.out.println(numero);

       System.out.println(++numero);

       System.out.println(numero --);

       boolean variavel = true;

       System.out.println(variavel);
       System.out.println(!variavel);
       System.out.println(variavel);

       variavel = !variavel;

       System.out.println(variavel);
    }
}
